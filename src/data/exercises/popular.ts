import type { Exercise } from "@/types";

type PopularExerciseInput = Omit<Exercise, "isCustom" | "youtubeUrl"> & {
  searchQuery: string;
};

function youtube(searchQuery: string): string {
  return `https://www.youtube.com/results?search_query=${encodeURIComponent(searchQuery)}`;
}

function exercise(input: PopularExerciseInput): Exercise {
  const { searchQuery, ...rest } = input;
  return {
    ...rest,
    youtubeUrl: youtube(searchQuery),
    isCustom: false,
  };
}

